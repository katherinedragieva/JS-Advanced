function sum(a, b) {
    return a + b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function subtract(a, b) {
    return a - b;
}
module.exports.sum = sum;
module.exports.multiply = multiply;
module.exports.divide = divide;
module.exports.subtract = subtract;
